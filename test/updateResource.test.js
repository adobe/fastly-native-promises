/* eslint-env mocha */
import assert from 'assert';
import nock from 'nock';
import config from '../src/config.js';
import fastlyPromises from '../src/index.js';
import response from './response/resource.response.js';

describe('#updateResource', () => {
  const fastly = fastlyPromises('923b6bd5266a7f932e41962755bd4254', 'SU1Z0isxPaozGVKXdv0eY');
  let res;

  nock(config.mainEntryPoint)
    .put('/service/SU1Z0isxPaozGVKXdv0eY/version/1/resource/store-id-1')
    .reply(200, response.put);

  before(async () => {
    res = await fastly.updateResource('1', 'store-id-1', 'updated-secrets');
  });

  it('response should be a status 200', () => {
    assert.strictEqual(res.status, 200);
  });

  it('response body should exist', () => {
    assert.ok(res.data);
  });

  it('response body should be an object', () => {
    assert.strictEqual(typeof res.data, 'object');
  });

  it('response body properties should be updated', () => {
    assert.strictEqual(res.data.name, 'updated-secrets');
    assert.ok(res.data.updated_at);
  });
});
